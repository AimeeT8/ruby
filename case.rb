weather = 'rainy'

case weather
when 'sunny'
    puts "It's time for a breezy summer dress"
when 'snow'
    puts "bundle up with a heavy winter coat and boots"
when 'windy'
    puts "Time to find that wind breaker from the 90's and pin your hair back!"
when 'cloudy'
    puts 'It might rain so bring a proper coat'
when 'rainy'
    puts "Stay at home and curl up with a good book"
else
    puts "Not sure about the weather today so wear what makes you feel good!"
end